def test_placeholder_records_pass_without_real_requirement(tmp_path: Path):
    path = write_records(tmp_path, [placeholder_record()])
    completed = run_cli(path)
    assert completed.returncode == 0
    assert "no scored records yet" in completed.stdout


def test_placeholder_records_fail_with_require_real_records(tmp_path: Path):
    path = write_records(tmp_path, [placeholder_record()])
    completed = run_cli(path, "--require-real-records")
    assert completed.returncode != 0
    assert "No scored image-output records exist" in completed.stdout


def test_scored_record_with_dicts_and_lists_passes_without_type_error(tmp_path: Path):
    path = write_records(tmp_path, [scored_record()])
    completed = run_cli(path)
    assert completed.returncode == 0, completed.stdout + completed.stderr
    assert "1 scored record" in completed.stdout


