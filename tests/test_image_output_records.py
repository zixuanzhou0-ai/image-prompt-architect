from __future__ import annotations

import importlib.util
import json
import subprocess
import sys
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
SCRIPT = ROOT / "evals" / "check_image_output_records.py"

spec = importlib.util.spec_from_file_location("check_image_output_records", SCRIPT)
check_image_output_records = importlib.util.module_from_spec(spec)
assert spec.loader is not None
sys.modules["check_image_output_records"] = check_image_output_records
spec.loader.exec_module(check_image_output_records)


def write_records(tmp_path: Path, records: list[dict]) -> Path:
    path = tmp_path / "image_output_records.json"
    path.write_text(json.dumps({"status": "test", "records": records}, indent=2), encoding="utf-8")
    return path


def run_cli(path: Path, *args: str) -> subprocess.CompletedProcess[str]:
    return subprocess.run(
        [sys.executable, str(SCRIPT), "--records", str(path), *args],
        cwd=ROOT,
        text=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
    )


